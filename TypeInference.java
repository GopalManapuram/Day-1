public class TypeInference {
    public static void main(String[] args) {
        
        var number = 10; 
        var message = "Hello, Java!"; 
        var decimalValue = 5.5; 
        var isJavaFun = true; 

        // Printing the values and their types
        System.out.println("Number (int): " + number);
        System.out.println("Message (String): " + message);
        System.out.println("Decimal Value (double): " + decimalValue);
        System.out.println("Is Java Fun? (boolean): " + isJavaFun);

        

       
        for (var i = 0; i < 5; i++) {
            System.out.println("Loop iteration: " + i);
        }
    }
}
